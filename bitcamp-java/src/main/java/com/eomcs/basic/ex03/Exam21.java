package com.eomcs.basic.ex03;

// 정수 리터럴(literal)

public class Exam21 {
  public static void main(String[] args) {
    // 10진수 리터럴
    System.out.println(100);  // 10진수 표기법
    
    // 8진수 리터럴
    System.out.println(0144);

    // 16진수 리터럴
    System.out.println(0x64);

    // 2진수 리터럴
    System.out.println(0b1100100);

    // 부동소수점 리터럴
    System.out.println(10000e-2);
    System.out.println(0.1e3);
  }
}